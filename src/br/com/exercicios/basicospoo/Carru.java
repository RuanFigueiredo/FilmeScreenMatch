package br.com.exercicios.basicospoo;

public class Carru {
    private String modelo;
    private int ano;
    private double precoMenor;

    private double precoMaior;

    public String getModelo() {
        return modelo;
    }

    public int getAno() {
        return ano;
    }



    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public double getPrecoMenor() {
        return precoMenor;
    }

    public double getPrecoMaior() {
        return precoMaior;
    }

    public void setPrecoMenor(double precoMenor) {
        this.precoMenor = precoMenor;
    }

    public void setPrecoMaior(double precoMaior) {
        this.precoMaior = precoMaior;
    }
}
